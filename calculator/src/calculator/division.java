
package calculator;

public class division {
    void div(int x,int y){
        double div =0;
        div=x/y;
        System.out.println("two numbers divsion is ="+div);
    }
    void div(int x,int y,int z){
        double div =0;
        div=x/y/z;
        System.out.println("three numbers divsion is ="+div);
    }
    void div(int x,int y,int z,int k){
        double div =0;
        div=x/y/z/k;
        System.out.println("four numbers divsion is ="+div);
    }
}
