
package calculator;

public class subtration {
    void sub(int x,int y){
        int sub=0;
        sub=x-y;
        System.out.println("two numbers subtration is ="+sub);
    }
    void sub(int x,int y,int z){
        int sub=0;
        sub=x-y-z;
        System.out.println("three numbers subtration is ="+sub);
    }
    void sub(int x,int y,int z,int k){
        int sub=0;
        sub=x-y-z-k;
        System.out.println("four numbers subtration is ="+sub);
    }
}
